package com.example.wechatimitation;

public class Friend {
    private int user_id;
    private String user_name;
    private String user_password;
    private int user_friedns;
    private int user_img;

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_password() {
        return user_password;
    }

    public void setUser_password(String user_password) {
        this.user_password = user_password;
    }

    public int getUser_friedns() {
        return user_friedns;
    }

    public void setUser_friedns(int user_friedns) {
        this.user_friedns = user_friedns;
    }

    public int getUser_img() {
        return user_img;
    }

    public void setUser_img(int user_img) {
        this.user_img = user_img;
    }
}
